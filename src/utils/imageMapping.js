// Helper function to get image by category and type
export const getImage = (category, type) => {
    return creditUnionImages[category]?.[type] || creditUnionImages.hero.modernBanking;
};

// Helper function to get random image from category
export const getRandomImage = (category) => {
    const categoryImages = creditUnionImages[category];
    if (!categoryImages) return creditUnionImages.hero.modernBanking;

    const keys = Object.keys(categoryImages);
    const randomKey = keys[Math.floor(Math.random() * keys.length)];
    return categoryImages[randomKey];
};

